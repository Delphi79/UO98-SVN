    /// <summary>
    /// Reason for invalid login provided by LoginDeniedPacket
    /// </summary>
    public enum LoginRejectReason : byte
    {
        /// <summary>We were unable to authenticate your login.  Usually this means that either a newer client patch is now available, or that the account name or password you provided are incorrect.  Please check your Caps Lock key and try reentering your account name and password.  Please restart your client if you continue to receive this error.  If this is a new account your account may not be active yet, please try again shortly.  If you continue to experience problems please contact customer support.</summary>
        UnknownUser = 0x00,
        /// <summary>Someone is already using this account.</summary>
        InUse = 0x01,
        /// <summary>Your account has been blocked. Please visit <A HREF=\"http://ultima-registration.com\">http://ultima-registration.com<A> for information on reactivating your account.</summary>
        Disabled = 0x02,
        /// <summary>Your account credentials are invalid. Check your user ID and password and try again.</summary>
        BadPass = 0x03,
        /// <summary>The IGR concurrency limit has been met.</summary>
        IGRconcurrency = 0x05,
        /// <summary>The IGR time limit has been met.</summary>
        IGRtime = 0x06,
        /// <summary>A general IGR authentication failure has occurred.</summary>
        IGRauth = 0x07,
        /// <summary>You cannot currently log into this shard because a character transfer is in progress. Transfers can take up to an hour. If you continue to receive this message after that time, please log into another shard and use the in-game Help Menu to page a Game Master for help.</summary>
        CharTransfer = 0x09,
        /// <summary>No message, client reconstructs it's socket or something...</summary>
        Idle = 0xFE,
        /// <summary>There is some problem communicating with Origin. Please restart Ultima Online and try again.</summary>
        CommFail = 0xFF
    }
